console.log('📰 Начинаем добавление новостей...');

db.serialize(() => {
  // Проверяем, есть ли уже новости
  db.get('SELECT COUNT(*) as count FROM news', (err, row) => {
    if (err) {
      console.error('Ошибка при проверке:', err);
      return;
    }
    
    if (row.count === 0) {
      // Добавляем новости
      const stmt = db.prepare('INSERT INTO news (title, content, date, category, image) VALUES (?, ?, ?, ?, ?)');
      
      newsData.forEach((news, index) => {
        stmt.run([news.title, news.content, news.date, news.category, news.image], function(err) {
          if (err) {
            console.error(`Ошибка при добавлении новости ${index + 1}:`, err);
          } else {
            console.log(`✅ Добавлена новость: ${news.title}`);
          }
        });
      });
      
      // Проверяем результат
      setTimeout(() => {
        db.get('SELECT COUNT(*) as count FROM news', (err, row) => {
          if (err) {
            console.error('Ошибка при подсчете:', err);
          } else {
            console.log(`\n📊 Всего новостей в базе: ${row.count}`);
          }
        });
      }, 500);
    } else {
      console.log('ℹ️ В базе уже есть новости, пропускаем добавление');
      console.log(`📊 Текущее количество новостей: ${row.count}`);
    }
  });
});

setTimeout(() => {
  db.close();
  console.log('\n✅ Готово!');
}, 2000);